"""
Ancilla-Assisted Metrology Optimisation via Nelder–Mead — Core Physics.

Implements the core operators, state preparation, circuit evolution, and
sensitivity computation for a two-qubit (system + ancilla) Mach–Zehnder
interferometer estimating an unknown phase rate ω.

Physical Model:
- Two qubits: system (S) and ancilla (A), each isomorphic to a spin-½.
- Basis: {|1,0⟩, |0,1⟩} for each qubit (single-particle subspace).
- Beam splitters: generated by J_x with optimisable duration T.
- Holding Hamiltonian:
    H_hold = ω J_z^S + H_int(α)
    H_int = α_xx J_x^S⊗J_x^A + α_xz J_x^S⊗J_z^A + α_zx J_z^S⊗J_x^A + α_zz J_z^S⊗J_z^A
- Measurement: J_z^S on the system qubit.
- Sensitivity: Δω via error propagation (central finite differences).

Units:
- Dimensionless throughout. ω is the unknown phase rate (radians per unit time).
- t_hold: holding-time strength (dimensionless).
- α coefficients: real coupling strengths.

References:
- Giovannetti, Lloyd, Maccone, Nat. Photonics 5, 222 (2011)
- Davis et al., PRA 94, 063814 (2016)
- Kitagawa & Ueda, PRA 47, 5138 (1993)
- Hentschel & Sanders, PRA 88, 062329 (2013)

"""

from __future__ import annotations

from dataclasses import dataclass
from functools import lru_cache

import numpy as np

from src.physics.beam_splitter import bs_qubit
from src.utils.constants import I_2, I_4, J_X, J_Y, J_Z, SIGMA_X, SIGMA_Z


@dataclass
class _DebugConfig:
    """Gated-debug configuration for operator assertions.

    Set ``verify_operators=True`` in unit tests to verify operator
    properties on every call.  When ``False`` (optimisation hot path),
    expensive assertions are skipped.
    """

    verify_operators: bool = False


# Module-level singleton.  Unit tests can set ``_debug.verify_operators = True``
# to enable cold-path assertion checks without changing call signatures.
_debug = _DebugConfig()


def _expm_hermitian(H: np.ndarray, t: float) -> np.ndarray:
    """Exponentiate a Hermitian matrix ``exp(-i t H)`` via ``eigh``.

    For a small (4×4) Hermitian matrix this is 2–3× faster than
    ``scipy.linalg.expm`` because it exploits Hermiticity.
    """
    eigvals, eigvecs = np.linalg.eigh(H)
    return eigvecs @ np.diag(np.exp(-1j * t * eigvals)) @ eigvecs.conj().T


