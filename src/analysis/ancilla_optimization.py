def single_qubit_state(theta: float, phi_Bloch: float) -> np.ndarray:
    """Create a single-qubit pure state on the Bloch sphere.

    |ψ⟩ = cos(θ/2) |0⟩ + e^{iφ} sin(θ/2) |1⟩

    where |0⟩ = |1,0⟩ (particle in mode 0), |1⟩ = |0,1⟩ (particle in mode 1).

    Args:
        theta: Polar angle ∈ [0, π].
        phi_Bloch: Azimuthal angle ∈ [0, 2π).

    Returns:
        Normalised complex 2-vector.

    """
    return np.array(
        [np.cos(theta / 2.0), np.exp(1j * phi_Bloch) * np.sin(theta / 2.0)],
        dtype=complex,
    )


def two_qubit_state(
    theta_S: float,
    phi_S: float,
    theta_A: float,
    phi_A: float,
) -> np.ndarray:
    """Create a product state |ψ_S⟩ ⊗ |ψ_A⟩.

    Args:
        theta_S: Polar angle for system qubit.
        phi_S: Azimuthal angle for system qubit.
        theta_A: Polar angle for ancilla qubit.
        phi_A: Azimuthal angle for ancilla qubit.

    Returns:
        Normalised complex 4-vector (product state).

    """
    psi_S = single_qubit_state(theta_S, phi_S)
    psi_A = single_qubit_state(theta_A, phi_A)
    return np.kron(psi_S, psi_A)


def free_ancilla_initial_state(theta_A: float, phi_A: float) -> np.ndarray:
    r"""Construct the system--ancilla product initial state.

    :math:`|\Psi_0\rangle = |1,0\rangle_S \otimes |\psi_A(\theta_A, \phi_A)\rangle`

    where :math:`|\psi_A\rangle = \cos(\theta_A/2)\,|1,0\rangle_A
    + e^{i\phi_A}\sin(\theta_A/2)\,|0,1\rangle_A`.

    Args:
        theta_A: Ancilla polar angle :math:`\in [0, \pi]`.
        phi_A: Ancilla azimuthal angle :math:`\in [0, 2\pi)`.

    Returns:
        Normalised 4-vector in the :math:`\{|00\rangle, |01\rangle, |10\rangle,
        |11\rangle\}` basis.
    """
    psi_S = np.array([1.0, 0.0], dtype=complex)  # |1,0⟩_S
    psi_A = single_qubit_state(theta_A, phi_A)
    state = np.kron(psi_S, psi_A)
    if _debug.verify_operators:
        assert np.isclose(np.linalg.norm(state), 1.0), (
            "Free-ancilla initial state must be normalised"
        )
    return state


