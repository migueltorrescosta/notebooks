def _params_to_components(
    params: np.ndarray,
) -> tuple[
    float,
    float,
    float,
    float,
    float,
    float,
    float,
    tuple[float, float, float, float],
]:
    """Unpack the 11-element parameter vector into named components.

    Parameter order:
        [θ_S, φ_S, θ_A, φ_A, T_BS1, T_BS2, t_hold, α_xx, α_xz, α_zx, α_zz]
        Note: θ_S, θ_A are Bloch sphere polar angles, NOT the phase rate ω.

    Args:
        params: 11-element array of optimisation parameters.

    Returns:
        Tuple (theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, t_hold, alpha).

    """
    theta_S = float(params[0])
    phi_S = float(params[1])
    theta_A = float(params[2])
    phi_A = float(params[3])
    T_BS1 = float(params[4])
    T_BS2 = float(params[5])
    t_hold = float(params[6])
    alpha: tuple[float, float, float, float] = (
        float(params[7]),
        float(params[8]),
        float(params[9]),
        float(params[10]),
    )
    return theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, t_hold, alpha


def _enforce_bounds(
    theta_S: float,
    theta_A: float,
    phi_S: float,
    phi_A: float,
    T_BS1: float,
    T_BS2: float,
    t_hold: float,
    alpha: tuple[float, float, float, float],
    bounds: dict[str, tuple[float, float]],
    penalty_scale: float = 1e6,
    fixed_alpha: tuple[float, float, float, float] | None = None,
) -> float:
    """Enforce parameter bounds via quadratic penalty.

    Args:
        theta_S: System polar angle.
        theta_A: Ancilla polar angle.
        phi_S: System azimuthal angle.
        phi_A: Ancilla azimuthal angle.
        T_BS1: First beam-splitter duration.
        T_BS2: Second beam-splitter duration.
        t_hold: Holding-time strength.
        alpha: 4-element interaction coefficient tuple.
        bounds: Dict of parameter bounds.
        penalty_scale: Scale for bound-violation penalty.
        fixed_alpha: If set, alpha bound checks are skipped.

    Returns:
        Total penalty (0.0 if no violation).

    """
    penalty = 0.0
    for val, key in [
        (theta_S, "bloch_theta"),
        (theta_A, "bloch_theta"),
        (phi_S, "phi"),
        (phi_A, "phi"),
        (T_BS1, "T_BS"),
        (T_BS2, "T_BS"),
        (t_hold, "t_hold"),
    ]:
        lo, hi = bounds[key]
        if val < lo:
            penalty += penalty_scale * (lo - val) ** 2
        if val > hi:
            penalty += penalty_scale * (val - hi) ** 2
    if fixed_alpha is None:
        for i in range(4):
            lo, hi = bounds["alpha"]
            if alpha[i] < lo:
                penalty += penalty_scale * (lo - alpha[i]) ** 2
            if alpha[i] > hi:
                penalty += penalty_scale * (alpha[i] - hi) ** 2
    return penalty


def sensitivity_objective(
    params: np.ndarray,
    omega_true: float,
    ops: dict[str, np.ndarray],
    fd_step: float = 1e-6,
    bounds: dict[str, tuple[float, float]] | None = None,
    penalty_scale: float = 1e6,
    meas_op: np.ndarray | None = None,
    fixed_alpha: tuple[float, float, float, float] | None = None,
) -> float:
    """Objective function for minimising Δω.

    Args:
        params: 11-element optimisation parameter vector, or 7-element
            state-only vector when fixed_alpha is set.
        omega_true: True phase rate parameter.
        ops: Two-qubit operators.
        fd_step: Finite-difference step size.
        bounds: Dict of parameter bounds (defaults to report values).
        penalty_scale: Scale for bound-violation penalty.
        meas_op: Measurement operator. Defaults to ops['Jz_S'].
        fixed_alpha: If set, params is a 7-element state vector;
            fixed_alpha is prepended to form the full 11-element vector.
            Alpha bound checks are skipped in this mode.

    Returns:
        Δω value (plus infinite penalty if bounds violated).

    """
    # Default bounds from the report
    if bounds is None:
        bounds = get_default_bounds()

    # If fixed_alpha is set, params is 7-element (state only);
    # prepend fixed_alpha to make 11 elements.
    if fixed_alpha is not None:
        params = np.concatenate([params, np.asarray(fixed_alpha, dtype=float)])

    theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, t_hold, alpha = _params_to_components(
        params,
    )

    # Bound enforcement (penalty method)
    penalty = _enforce_bounds(
        theta_S,
        theta_A,
        phi_S,
        phi_A,
        T_BS1,
        T_BS2,
        t_hold,
        alpha,
        bounds,
        penalty_scale,
        fixed_alpha,
    )
    if penalty > 0.0:
        return float(1e10 + penalty)

    # Build initial state
    psi0 = two_qubit_state(theta_S, phi_S, theta_A, phi_A)

    # Compute sensitivity
    return compute_sensitivity(
        psi0,
        T_BS1,
        T_BS2,
        t_hold,
        omega_true,
        alpha,
        ops,
        fd_step,
        meas_op=meas_op,
    )


