@dataclass
class AlphaSingleScanResult:
    """Result from scanning a single α coefficient while holding others fixed.

    Attributes:
        alpha_name: Which coefficient was scanned ('xx', 'xz', 'zx', 'zz').
        alpha_values: Array of α values scanned.
        delta_omega_values: Corresponding Δω sensitivity values.
        fixed_params: Dict of the other fixed parameters used.

    """

    alpha_name: str
    alpha_values: np.ndarray
    delta_omega_values: np.ndarray
    fixed_params: dict[str, float] = field(default_factory=dict)

    def to_dataframe(self) -> pd.DataFrame:
        """Flatten the single-α scan into a DataFrame.

        Returns:
            DataFrame with columns: alpha_name, alpha, delta_omega,
            and fixed_params serialised as JSON.

        """
        return pd.DataFrame(
            {
                "alpha_name": [self.alpha_name] * len(self.alpha_values),
                "alpha": self.alpha_values,
                "delta_omega": self.delta_omega_values,
                "fixed_params_json": [json.dumps(self.fixed_params)]
                * len(self.alpha_values),
            },
        )

    def save_parquet(self, path: str | Path) -> Path:
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        self.to_dataframe().to_parquet(path, index=False)
        return path

    @classmethod
    def from_parquet(cls, path: str | Path) -> AlphaSingleScanResult:
        df = pd.read_parquet(path)
        required = {"alpha_name", "alpha", "delta_omega", "fixed_params_json"}
        missing = required - set(df.columns)
        if missing:
            raise ValueError(
                f"Parquet at {path} is missing required columns: "
                f"{sorted(missing)}. Regenerate the file with the current code."
            )
        return cls(
            alpha_name=str(df["alpha_name"].iloc[0]),
            alpha_values=df["alpha"].to_numpy(dtype=float),
            delta_omega_values=df["delta_omega"].to_numpy(dtype=float),
            fixed_params=json.loads(str(df["fixed_params_json"].iloc[0])),
        )


@dataclass
class AlphaRandomSearchResult:
    """Result from random search over the 4D α coefficient space.

    Attributes:
        alpha_samples: Array of shape (N, 4) with sampled α values.
        delta_omega_values: Array of shape (N,) with Δω for each sample.
        best_alpha: The α = (α_xx, α_xz, α_zx, α_zz) that gave minimal Δω.
        best_delta_omega: The minimal Δω found.
        fixed_params: Dict of the other fixed parameters used.

    """

    alpha_samples: np.ndarray
    delta_omega_values: np.ndarray
    best_alpha: tuple[float, float, float, float]
    best_delta_omega: float
    fixed_params: dict[str, float] = field(default_factory=dict)

    def to_dataframe(self) -> pd.DataFrame:
        """Flatten the random search into a DataFrame.

        Returns:
            DataFrame with columns: alpha_xx, alpha_xz, alpha_zx,
            alpha_zz, delta_omega, fixed_params_json.

        """
        return pd.DataFrame(
            {
                "alpha_xx": self.alpha_samples[:, 0],
                "alpha_xz": self.alpha_samples[:, 1],
                "alpha_zx": self.alpha_samples[:, 2],
                "alpha_zz": self.alpha_samples[:, 3],
                "delta_omega": self.delta_omega_values,
                "fixed_params_json": [json.dumps(self.fixed_params)]
                * len(self.delta_omega_values),
            },
        )

    def save_parquet(self, path: str | Path) -> Path:
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        self.to_dataframe().to_parquet(path, index=False)
        return path

    @classmethod
    def from_parquet(cls, path: str | Path) -> AlphaRandomSearchResult:
        df = pd.read_parquet(path)
        required = {
            "alpha_xx",
            "alpha_xz",
            "alpha_zx",
            "alpha_zz",
            "delta_omega",
            "fixed_params_json",
        }
        missing = required - set(df.columns)
        if missing:
            raise ValueError(
                f"Parquet at {path} is missing required columns: "
                f"{sorted(missing)}. Regenerate the file with the current code."
            )
        alphas = df[["alpha_xx", "alpha_xz", "alpha_zx", "alpha_zz"]].to_numpy(
            dtype=float
        )
        deltas = df["delta_omega"].to_numpy(dtype=float)
        best_idx = int(np.argmin(deltas))
        return cls(
            alpha_samples=alphas,
            delta_omega_values=deltas,
            best_alpha=(
                float(alphas[best_idx, 0]),
                float(alphas[best_idx, 1]),
                float(alphas[best_idx, 2]),
                float(alphas[best_idx, 3]),
            ),
            best_delta_omega=float(deltas[best_idx]),
            fixed_params=json.loads(str(df["fixed_params_json"].iloc[0])),
        )


def scan_alpha_single_parameter(
    alpha_name: str,
    alpha_min: float = -2.0,
    alpha_max: float = 2.0,
    n_points: int = 21,
    *,
    theta_S: float = np.pi / 2,
    phi_S: float = 0.0,
    theta_A: float = 0.0,
    phi_A: float = 0.0,
    T_BS1: float = np.pi / 2,
    T_BS2: float = np.pi / 2,
    t_hold: float = 1.0,
    omega_true: float = 1.0,
    meas_op: np.ndarray | None = None,
) -> AlphaSingleScanResult:
    """Scan a single α coefficient while holding others fixed.

    This replicates the grid scan described in the report: "each α_ij is
    scanned independently in [-2, 2] with 21 points, while the other three
    are held at zero."

    Physical configuration defaults (SQL-optimal for α=0):
        - System: |ψ_S⟩ = (|0⟩ + |1⟩)/√2 (θ_S = π/2)
        - Ancilla: |ψ_A⟩ = |0⟩ (θ_A = 0)
        - Beam splitters: 50/50 (T_BS1 = T_BS2 = π/2)
        - t_hold = 1.0, ω_true = 1.0

    Args:
        alpha_name: Which coefficient to scan: 'xx', 'xz', 'zx', or 'zz'.
        alpha_min: Minimum value for the scan (default -2.0).
        alpha_max: Maximum value for the scan (default 2.0).
        n_points: Number of points in the scan (default 21).
        theta_S: System polar angle (default π/2).
        phi_S: System azimuthal angle (default 0.0).
        theta_A: Ancilla polar angle (default 0.0).
        phi_A: Ancilla azimuthal angle (default 0.0).
        T_BS1: First beam-splitter duration (default π/2).
        T_BS2: Second beam-splitter duration (default π/2).
        t_hold: Holding time (default 1.0).
        omega_true: True phase rate (default 1.0).
        meas_op: Measurement operator. Defaults to ops['Jz_S'] (S-only).

    Returns:
        AlphaSingleScanResult with scanned values and sensitivities.

    Raises:
        ValueError: If alpha_name is not one of 'xx', 'xz', 'zx', 'zz'.

    """
    alpha_idx_map = {"xx": 0, "xz": 1, "zx": 2, "zz": 3}
    if alpha_name not in alpha_idx_map:
        raise ValueError(
            f"alpha_name must be one of {list(alpha_idx_map.keys())}, got {alpha_name}",
        )
    scan_idx = alpha_idx_map[alpha_name]

    ops = build_two_qubit_operators()
    if meas_op is None:
        meas_op = ops["Jz_S"]
    psi0 = two_qubit_state(theta_S, phi_S, theta_A, phi_A)

    alpha_values = np.linspace(alpha_min, alpha_max, n_points)
    delta_omega_values = np.zeros(n_points, dtype=float)

    for i, a_val in enumerate(alpha_values):
        alpha_list = [0.0, 0.0, 0.0, 0.0]
        alpha_list[scan_idx] = a_val
        alpha: tuple[float, float, float, float] = (
            alpha_list[0],
            alpha_list[1],
            alpha_list[2],
            alpha_list[3],
        )
        domega = compute_sensitivity(
            psi0,
            T_BS1,
            T_BS2,
            t_hold,
            omega_true,
            alpha,
            ops,
            meas_op=meas_op,
        )
        delta_omega_values[i] = domega

    fixed_params = {
        "theta_S": theta_S,
        "phi_S": phi_S,
        "theta_A": theta_A,
        "phi_A": phi_A,
        "T_BS1": T_BS1,
        "T_BS2": T_BS2,
        "t_hold": t_hold,
        "omega_true": omega_true,
    }

    return AlphaSingleScanResult(
        alpha_name=alpha_name,
        alpha_values=alpha_values,
        delta_omega_values=delta_omega_values,
        fixed_params=fixed_params,
    )


def random_search_alpha(
    n_samples: int = 200,
    alpha_min: float = -2.0,
    alpha_max: float = 2.0,
    *,
    theta_S: float = np.pi / 2,
    phi_S: float = 0.0,
    theta_A: float = 0.0,
    phi_A: float = 0.0,
    T_BS1: float = np.pi / 2,
    T_BS2: float = np.pi / 2,
    t_hold: float = 1.0,
    omega_true: float = 1.0,
    seed: int | None = 42,
    meas_op: np.ndarray | None = None,
) -> AlphaRandomSearchResult:
    """Random search over the 4D α = (α_xx, α_xz, α_zx, α_zz) space.

    This replicates the random search described in the report: "200 samples
    over the full 4D α space" to verify that no combination achieves
    Δω < 1/t_hold (SQL).

    Args:
        n_samples: Number of random α samples to evaluate (default 200).
        alpha_min: Lower bound for all α coefficients (default -2.0).
        alpha_max: Upper bound for all α coefficients (default 2.0).
        theta_S: System polar angle (default π/2).
        phi_S: System azimuthal angle (default 0.0).
        theta_A: Ancilla polar angle (default 0.0).
        phi_A: Ancilla azimuthal angle (default 0.0).
        T_BS1: First beam-splitter duration (default π/2).
        T_BS2: Second beam-splitter duration (default π/2).
        t_hold: Holding time (default 1.0).
        omega_true: True phase rate (default 1.0).
        seed: Random seed for reproducibility (default 42).
        meas_op: Measurement operator. Defaults to ops['Jz_S'] (S-only).

    Returns:
        AlphaRandomSearchResult with all samples and the best found.

    """
    rng = np.random.default_rng(seed)
    ops = build_two_qubit_operators()
    if meas_op is None:
        meas_op = ops["Jz_S"]
    psi0 = two_qubit_state(theta_S, phi_S, theta_A, phi_A)

    # Sample uniformly in [alpha_min, alpha_max]^4
    alpha_samples = rng.uniform(alpha_min, alpha_max, size=(n_samples, 4))
    delta_omega_values = np.zeros(n_samples, dtype=float)

    for i in range(n_samples):
        alpha: tuple[float, float, float, float] = (
            float(alpha_samples[i, 0]),
            float(alpha_samples[i, 1]),
            float(alpha_samples[i, 2]),
            float(alpha_samples[i, 3]),
        )
        domega = compute_sensitivity(
            psi0,
            T_BS1,
            T_BS2,
            t_hold,
            omega_true,
            alpha,
            ops,
            meas_op=meas_op,
        )
        delta_omega_values[i] = domega

    # Find best (minimal Δω)
    best_idx = int(np.argmin(delta_omega_values))
    best_alpha: tuple[float, float, float, float] = (
        float(alpha_samples[best_idx, 0]),
        float(alpha_samples[best_idx, 1]),
        float(alpha_samples[best_idx, 2]),
        float(alpha_samples[best_idx, 3]),
    )
    best_delta_omega = float(delta_omega_values[best_idx])

    fixed_params = {
        "theta_S": theta_S,
        "phi_S": phi_S,
        "theta_A": theta_A,
        "phi_A": phi_A,
        "T_BS1": T_BS1,
        "T_BS2": T_BS2,
        "t_hold": t_hold,
        "omega_true": omega_true,
    }

    return AlphaRandomSearchResult(
        alpha_samples=alpha_samples,
        delta_omega_values=delta_omega_values,
        best_alpha=best_alpha,
        best_delta_omega=best_delta_omega,
        fixed_params=fixed_params,
    )


