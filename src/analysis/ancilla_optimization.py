"""
Ancilla-Assisted Metrology Optimization via Nelder–Mead.

Implements the full-parameter optimisation of a two-qubit (system + ancilla)
Mach–Zehnder interferometer for estimating an unknown phase rate θ.

Physical Model:
- Two qubits: system (S) and ancilla (A), each isomorphic to a spin-½.
- Basis: {|1,0⟩, |0,1⟩} for each qubit (single-particle subspace).
- Beam splitters: generated by J_x with optimisable duration T.
- Holding Hamiltonian:
    H_hold = θ J_z^S + H_int(α)
    H_int = α_xx J_x^S⊗J_x^A + α_xz J_x^S⊗J_z^A + α_zx J_z^S⊗J_x^A + α_zz J_z^S⊗J_z^A
- Measurement: J_z^S on the system qubit.
- Sensitivity: Δθ via error propagation (central finite differences).

Units:
- Dimensionless throughout. θ is the unknown phase rate (radians per unit time).
- T_H: holding-time strength (dimensionless).
- α coefficients: real coupling strengths.

References:
- Giovannetti, Lloyd, Maccone, Nat. Photonics 5, 222 (2011)
- Davis et al., PRA 94, 063814 (2016)
- Kitagawa & Ueda, PRA 47, 5138 (1993)
- Hentschel & Sanders, PRA 88, 062329 (2013)
"""

from __future__ import annotations

from dataclasses import dataclass, field

import numpy as np
from scipy.linalg import expm
from scipy.optimize import minimize


# ============================================================================
# Physical Constants & Pauli Matrices
# ============================================================================

# Pauli matrices (dimensionless)
SIGMA_X: np.ndarray = np.array([[0, 1], [1, 0]], dtype=complex)
SIGMA_Y: np.ndarray = np.array([[0, -1j], [1j, 0]], dtype=complex)
SIGMA_Z: np.ndarray = np.array([[1, 0], [0, -1]], dtype=complex)
I_2: np.ndarray = np.eye(2, dtype=complex)

# Angular-momentum operators for a single qubit (J = σ/2)
J_X: np.ndarray = SIGMA_X / 2.0
J_Y: np.ndarray = SIGMA_Y / 2.0
J_Z: np.ndarray = SIGMA_Z / 2.0


# ============================================================================
# Operator Construction
# ============================================================================


def build_two_qubit_operators() -> dict[str, np.ndarray]:
    """Build the 4×4 operators for the two-qubit system.

    Basis ordering (computational basis):
        |0⟩_S|0⟩_A, |0⟩_S|1⟩_A, |1⟩_S|0⟩_A, |1⟩_S|1⟩_A
    where |0⟩ = |1,0⟩ (particle in mode 0), |1⟩ = |0,1⟩ (particle in mode 1).

    Returns:
        Dict with keys 'Jz_S', 'Jz_A', 'Jx_S', 'Jx_A', 'Jy_S', 'Jy_A',
        each a 4×4 complex Hermitian matrix.
    """
    return {
        "Jz_S": np.kron(J_Z, I_2),
        "Jz_A": np.kron(I_2, J_Z),
        "Jx_S": np.kron(J_X, I_2),
        "Jx_A": np.kron(I_2, J_X),
        "Jy_S": np.kron(J_Y, I_2),
        "Jy_A": np.kron(I_2, J_Y),
    }


def build_interaction_hamiltonian(
    alpha: tuple[float, float, float, float],
) -> np.ndarray:
    """Build the system–ancilla interaction Hamiltonian.

    H_int = α_xx · J_x^S ⊗ J_x^A + α_xz · J_x^S ⊗ J_z^A
          + α_zx · J_z^S ⊗ J_x^A + α_zz · J_z^S ⊗ J_z^A

    Args:
        alpha: (α_xx, α_xz, α_zx, α_zz) coupling coefficients.

    Returns:
        4×4 Hermitian interaction matrix.
    """
    a_xx, a_xz, a_zx, a_zz = alpha

    # Tensor-product operators: J_i^S ⊗ J_j^A  =  (σ_i/2) ⊗ (σ_j/2) = σ_i ⊗ σ_j / 4
    # Pre-compute the four tensor products
    xx = np.kron(SIGMA_X / 2, SIGMA_X / 2)  # J_x^S ⊗ J_x^A
    xz = np.kron(SIGMA_X / 2, SIGMA_Z / 2)  # J_x^S ⊗ J_z^A
    zx = np.kron(SIGMA_Z / 2, SIGMA_X / 2)  # J_z^S ⊗ J_x^A
    zz = np.kron(SIGMA_Z / 2, SIGMA_Z / 2)  # J_z^S ⊗ J_z^A

    H = np.zeros((4, 4), dtype=complex)
    if a_xx != 0.0:
        H += a_xx * xx
    if a_xz != 0.0:
        H += a_xz * xz
    if a_zx != 0.0:
        H += a_zx * zx
    if a_zz != 0.0:
        H += a_zz * zz
    return H


# ============================================================================
# State Preparation
# ============================================================================


def single_qubit_state(theta: float, phi: float) -> np.ndarray:
    """Create a single-qubit pure state on the Bloch sphere.

    |ψ⟩ = cos(θ/2) |0⟩ + e^{iφ} sin(θ/2) |1⟩

    where |0⟩ = |1,0⟩ (particle in mode 0), |1⟩ = |0,1⟩ (particle in mode 1).

    Args:
        theta: Polar angle ∈ [0, π].
        phi: Azimuthal angle ∈ [0, 2π).

    Returns:
        Normalised complex 2-vector.
    """
    return np.array(
        [np.cos(theta / 2.0), np.exp(1j * phi) * np.sin(theta / 2.0)], dtype=complex
    )


def two_qubit_state(
    theta_S: float, phi_S: float, theta_A: float, phi_A: float
) -> np.ndarray:
    """Create a product state |ψ_S⟩ ⊗ |ψ_A⟩.

    Args:
        theta_S: Polar angle for system qubit.
        phi_S: Azimuthal angle for system qubit.
        theta_A: Polar angle for ancilla qubit.
        phi_A: Azimuthal angle for ancilla qubit.

    Returns:
        Normalised complex 4-vector (product state).
    """
    psi_S = single_qubit_state(theta_S, phi_S)
    psi_A = single_qubit_state(theta_A, phi_A)
    return np.kron(psi_S, psi_A)


# ============================================================================
# Beam-Splitter Unitaries
# ============================================================================


def bs_unitary(T: float) -> np.ndarray:
    """Single-qubit beam-splitter unitary generated by J_x.

    U_BS(T) = exp(-i T J_x) = cos(T/2) I - i sin(T/2) σ_x

    A 50/50 beam splitter corresponds to T = π/2.

    Args:
        T: Beam-splitter duration (optimisable parameter).

    Returns:
        2×2 unitary matrix.
    """
    U = np.cos(T / 2.0) * I_2 - 1j * np.sin(T / 2.0) * SIGMA_X
    assert np.allclose(U @ U.conj().T, I_2, atol=1e-12), (
        f"BS unitary not unitary for T={T}"
    )
    return U


def two_qubit_bs_unitary(T: float) -> np.ndarray:
    """Two-qubit beam-splitter unitary: U_BS(T) ⊗ U_BS(T).

    Args:
        T: Beam-splitter duration.

    Returns:
        4×4 unitary matrix (tensor product).
    """
    U = bs_unitary(T)
    return np.kron(U, U)


# ============================================================================
# Holding Hamiltonian & Unitary
# ============================================================================


def build_hold_hamiltonian(
    theta: float,
    alpha: tuple[float, float, float, float],
    ops: dict[str, np.ndarray],
) -> np.ndarray:
    """Build the full holding Hamiltonian.

    H_hold = θ J_z^S + H_int(α)

    Args:
        theta: True phase rate parameter.
        alpha: (α_xx, α_xz, α_zx, α_zz) coupling coefficients.
        ops: Two-qubit operators from build_two_qubit_operators().

    Returns:
        4×4 Hermitian Hamiltonian matrix.
    """
    H_int = build_interaction_hamiltonian(alpha)
    H = theta * ops["Jz_S"] + H_int
    # Ensure Hermiticity
    H = 0.5 * (H + H.conj().T)
    return H


def hold_unitary(
    T_H: float,
    theta: float,
    alpha: tuple[float, float, float, float],
    ops: dict[str, np.ndarray],
) -> np.ndarray:
    """Holding-time unitary.

    U_hold(T_H) = exp(-i T_H H_hold)

    Args:
        T_H: Holding-time strength.
        theta: True phase rate parameter.
        alpha: (α_xx, α_xz, α_zx, α_zz) coupling coefficients.
        ops: Two-qubit operators from build_two_qubit_operators().

    Returns:
        4×4 unitary matrix.
    """
    H = build_hold_hamiltonian(theta, alpha, ops)
    U = expm(-1j * T_H * H)
    I_4 = np.eye(4, dtype=complex)
    assert np.allclose(U @ U.conj().T, I_4, atol=1e-12), (
        f"Hold unitary not unitary for T_H={T_H}, θ={theta}, α={alpha}"
    )
    return U


# ============================================================================
# Full Circuit Evolution
# ============================================================================


def evolve_full(
    psi0: np.ndarray,
    T_BS1: float,
    T_BS2: float,
    T_H: float,
    theta: float,
    alpha: tuple[float, float, float, float],
    ops: dict[str, np.ndarray],
) -> np.ndarray:
    """Run the full MZI circuit.

    |ψ_final⟩ = U_BS(T_BS2) · U_hold(T_H, θ, α) · U_BS(T_BS1) · |ψ₀⟩

    Args:
        psi0: Initial 4-vector (product state).
        T_BS1: First beam-splitter duration.
        T_BS2: Second beam-splitter duration.
        T_H: Holding-time strength.
        theta: True phase rate parameter.
        alpha: (α_xx, α_xz, α_zx, α_zz) coupling coefficients.
        ops: Two-qubit operators.

    Returns:
        Final 4-vector state.

    Raises:
        AssertionError: If input state is not normalised.
    """
    assert np.isclose(np.linalg.norm(psi0), 1.0), "Initial state must be normalised"

    psi = two_qubit_bs_unitary(T_BS1) @ psi0
    psi = hold_unitary(T_H, theta, alpha, ops) @ psi
    psi = two_qubit_bs_unitary(T_BS2) @ psi

    assert np.isclose(np.linalg.norm(psi), 1.0), "Final state must be normalised"
    return psi


# ============================================================================
# Sensitivity Computation (Error Propagation)
# ============================================================================


def compute_expectation_and_variance(
    psi: np.ndarray, operator: np.ndarray
) -> tuple[float, float]:
    """Compute ⟨ψ|O|ψ⟩ and Var(O) = ⟨O²⟩ - ⟨O⟩² for a pure state.

    Args:
        psi: Normalised state vector.
        operator: Hermitian operator matrix.

    Returns:
        Tuple (expectation, variance).
    """
    exp_val = np.real(psi.conj() @ operator @ psi)
    op_sq = operator @ operator
    exp_sq = np.real(psi.conj() @ op_sq @ psi)
    raw_var = exp_sq - exp_val**2
    # Numerical rounding can produce tiny negative variances (~1e-16).
    # Assert that variance is not significantly negative (physics error).
    assert raw_var >= -1e-12, (
        f"Unphysical negative variance: Var = {raw_var:.2e} (expectation = {exp_val:.6f})"
    )
    # Clamp residual round-off noise (typically O(1e-16)) to zero.
    var_val = max(0.0, raw_var)
    return float(exp_val), float(var_val)


def compute_reduced_purity(psi: np.ndarray) -> float:
    """Purity of the reduced density matrix of the system qubit.

    For a two-qubit pure state |ψ⟩ ∈ ℂ⁴ in the basis
    {|00⟩, |01⟩, |10⟩, |11⟩}, the reduced density matrix of the
    system qubit is ρ_S = Tr_A(|ψ⟩⟨ψ|).  Purity = Tr(ρ_S²).

    For a product state, purity = 1.  For a maximally entangled
    Bell state, purity = 0.5.

    Args:
        psi: Normalised 4-vector two-qubit state.

    Returns:
        Purity Tr(ρ_S²) ∈ [0.5, 1.0].
    """
    # psi components: [⟨00|ψ⟩, ⟨01|ψ⟩, ⟨10|ψ⟩, ⟨11|ψ⟩]
    # Reduced density matrix ρ_S = Tr_A(|ψ⟩⟨ψ|):
    #   ρ_S[0,0] = |⟨00|ψ⟩|² + |⟨01|ψ⟩|²   (prob. system in |0⟩)
    #   ρ_S[1,1] = |⟨10|ψ⟩|² + |⟨11|ψ⟩|²   (prob. system in |1⟩)
    #   ρ_S[0,1] = ⟨00|ψ⟩⟨ψ|10⟩ + ⟨01|ψ⟩⟨ψ|11⟩  (coherence)
    rho_00 = abs(psi[0]) ** 2 + abs(psi[1]) ** 2
    rho_11 = abs(psi[2]) ** 2 + abs(psi[3]) ** 2
    rho_01 = psi[0] * np.conj(psi[2]) + psi[1] * np.conj(psi[3])
    purity = rho_00**2 + rho_11**2 + 2.0 * abs(rho_01) ** 2
    # Assert the mathematically expected range [0.5, 1.0].
    # Outside this range indicates a computation error, not numerical noise.
    assert 0.5 - 1e-12 <= purity <= 1.0 + 1e-12, (
        f"Purity {purity:.15f} outside expected range [0.5, 1.0]"
    )
    return float(purity)


def compute_sensitivity(
    psi0: np.ndarray,
    T_BS1: float,
    T_BS2: float,
    T_H: float,
    theta_true: float,
    alpha: tuple[float, float, float, float],
    ops: dict[str, np.ndarray],
    fd_step: float = 1e-6,
) -> float:
    """Compute the error-propagation sensitivity Δθ.

    Δθ = sqrt(Var(J_z^S)) / |∂⟨J_z^S⟩/∂θ|

    The derivative is computed via central finite differences.

    Args:
        psi0: Initial 4-vector (product state).
        T_BS1: First beam-splitter duration.
        T_BS2: Second beam-splitter duration.
        T_H: Holding-time strength.
        theta_true: True phase rate parameter.
        alpha: (α_xx, α_xz, α_zx, α_zz) coupling coefficients.
        ops: Two-qubit operators (must contain 'Jz_S').
        fd_step: Finite-difference step size (default 1e-6).

    Returns:
        Sensitivity Δθ (positive float).

    Raises:
        RuntimeError: If derivative is zero (operating at fringe extremum).
    """
    Jz_S = ops["Jz_S"]

    # Evaluate at theta_true
    psi = evolve_full(psi0, T_BS1, T_BS2, T_H, theta_true, alpha, ops)
    _, var = compute_expectation_and_variance(psi, Jz_S)

    # Central finite difference for ∂⟨Jz_S⟩/∂θ
    psi_plus = evolve_full(psi0, T_BS1, T_BS2, T_H, theta_true + fd_step, alpha, ops)
    psi_minus = evolve_full(psi0, T_BS1, T_BS2, T_H, theta_true - fd_step, alpha, ops)
    exp_plus = np.real(psi_plus.conj() @ Jz_S @ psi_plus)
    exp_minus = np.real(psi_minus.conj() @ Jz_S @ psi_minus)
    d_exp = (exp_plus - exp_minus) / (2.0 * fd_step)

    if abs(d_exp) < 1e-15:
        # Derivative is effectively zero → fringe extremum
        return float("inf")

    return float(np.sqrt(var) / abs(d_exp))


# ============================================================================
# Objective Function for Optimisation
# ============================================================================


def _params_to_components(
    params: np.ndarray,
) -> tuple[
    float, float, float, float, float, float, float, tuple[float, float, float, float]
]:
    """Unpack the 11-element parameter vector into named components.

    Parameter order:
        [θ_S, φ_S, θ_A, φ_A, T_BS1, T_BS2, T_H, α_xx, α_xz, α_zx, α_zz]

    Args:
        params: 11-element array of optimisation parameters.

    Returns:
        Tuple (theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, T_H, alpha).
    """
    theta_S = float(params[0])
    phi_S = float(params[1])
    theta_A = float(params[2])
    phi_A = float(params[3])
    T_BS1 = float(params[4])
    T_BS2 = float(params[5])
    T_H = float(params[6])
    alpha: tuple[float, float, float, float] = (
        float(params[7]),
        float(params[8]),
        float(params[9]),
        float(params[10]),
    )
    return theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, T_H, alpha


def sensitivity_objective(
    params: np.ndarray,
    theta_true: float,
    ops: dict[str, np.ndarray],
    fd_step: float = 1e-6,
    bounds: dict[str, tuple[float, float]] | None = None,
    penalty_scale: float = 1e6,
) -> float:
    """Objective function for minimising Δθ.

    Args:
        params: 11-element optimisation parameter vector.
        theta_true: True phase rate parameter.
        ops: Two-qubit operators.
        fd_step: Finite-difference step size.
        bounds: Dict of parameter bounds (defaults to article values).
        penalty_scale: Scale for bound-violation penalty.

    Returns:
        Δθ value (plus infinite penalty if bounds violated).
    """
    # Default bounds from the article
    if bounds is None:
        bounds = get_default_bounds()

    theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, T_H, alpha = _params_to_components(
        params
    )

    # --- Bound enforcement (penalty method) ---
    penalty = 0.0
    for val, key in [
        (theta_S, "theta"),
        (theta_A, "theta"),
        (phi_S, "phi"),
        (phi_A, "phi"),
        (T_BS1, "T_BS"),
        (T_BS2, "T_BS"),
        (T_H, "T_H"),
    ]:
        lo, hi = bounds[key]
        if val < lo:
            penalty += penalty_scale * (lo - val) ** 2
        if val > hi:
            penalty += penalty_scale * (val - hi) ** 2
    for i in range(4):
        lo, hi = bounds["alpha"]
        if alpha[i] < lo:
            penalty += penalty_scale * (lo - alpha[i]) ** 2
        if alpha[i] > hi:
            penalty += penalty_scale * (alpha[i] - hi) ** 2

    if penalty > 0.0:
        return float(1e10 + penalty)

    # Build initial state
    psi0 = two_qubit_state(theta_S, phi_S, theta_A, phi_A)

    # Compute sensitivity
    dtheta = compute_sensitivity(
        psi0, T_BS1, T_BS2, T_H, theta_true, alpha, ops, fd_step
    )
    return dtheta


