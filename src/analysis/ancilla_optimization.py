def build_joint_operator(ops: dict[str, np.ndarray]) -> np.ndarray:
    """Build the joint measurement operator M = J_z^S + J_z^A.

    Args:
        ops: Two-qubit operators from build_two_qubit_operators().

    Returns:
        4×4 Hermitian matrix representing the joint measurement.

    """
    return ops["Jz_S"] + ops["Jz_A"]


def compute_covariance(
    psi: np.ndarray,
    ops: dict[str, np.ndarray],
) -> float:
    """Compute Cov(J_z^S, J_z^A) = ⟨J_z^S J_z^A⟩ - ⟨J_z^S⟩⟨J_z^A⟩.

    Args:
        psi: Normalised state vector (4-component).
        ops: Two-qubit operators (must contain 'Jz_S', 'Jz_A').

    Returns:
        Covariance between J_z^S and J_z^A measurements.

    """
    Jz_S = ops["Jz_S"]
    Jz_A = ops["Jz_A"]

    exp_S = np.real(psi.conj() @ Jz_S @ psi)
    exp_A = np.real(psi.conj() @ Jz_A @ psi)
    exp_SA = np.real(psi.conj() @ (Jz_S @ Jz_A) @ psi)

    cov = exp_SA - exp_S * exp_A
    # Covariance can be legitimately negative (anti-correlation).
    # Only check for NaN/Inf and that it lies in the mathematically
    # allowed range: |Cov(X,Y)| ≤ sqrt(Var(X) * Var(Y)).
    assert np.isfinite(cov), f"Covariance is not finite: Cov = {cov}"
    return float(cov)


def compute_expectation_and_variance(
    psi: np.ndarray,
    operator: np.ndarray,
) -> tuple[float, float]:
    """Compute ⟨ψ|O|ψ⟩ and Var(O) = ⟨O²⟩ - ⟨O⟩² for a pure state.

    Args:
        psi: Normalised state vector.
        operator: Hermitian operator matrix.

    Returns:
        Tuple (expectation, variance).

    """
    exp_val = np.real(psi.conj() @ operator @ psi)
    op_sq = operator @ operator
    exp_sq = np.real(psi.conj() @ op_sq @ psi)
    raw_var = exp_sq - exp_val**2
    # Numerical rounding can produce tiny negative variances (~1e-16).
    # Assert that variance is not significantly negative (physics error).
    assert raw_var >= -1e-12, (
        f"Unphysical negative variance: Var = {raw_var:.2e} (expectation = {exp_val:.6f})"
    )
    # Clamp residual round-off noise (typically O(1e-16)) to zero.
    var_val = max(0.0, raw_var)
    return float(exp_val), float(var_val)


def compute_reduced_purity(psi: np.ndarray) -> float:
    """Purity of the reduced density matrix of the system qubit.

    For a two-qubit pure state |ψ⟩ ∈ ℂ⁴ in the basis
    {|00⟩, |01⟩, |10⟩, |11⟩}, the reduced density matrix of the
    system qubit is ρ_S = Tr_A(|ψ⟩⟨ψ|).  Purity = Tr(ρ_S²).

    For a product state, purity = 1.  For a maximally entangled
    Bell state, purity = 0.5.

    Args:
        psi: Normalised 4-vector two-qubit state.

    Returns:
        Purity Tr(ρ_S²) ∈ [0.5, 1.0].

    """
    # psi components: [⟨00|ψ⟩, ⟨01|ψ⟩, ⟨10|ψ⟩, ⟨11|ψ⟩]
    # Reduced density matrix ρ_S = Tr_A(|ψ⟩⟨ψ|):
    #   ρ_S[0,0] = |⟨00|ψ⟩|² + |⟨01|ψ⟩|²   (prob. system in |0⟩)
    #   ρ_S[1,1] = |⟨10|ψ⟩|² + |⟨11|ψ⟩|²   (prob. system in |1⟩)
    #   ρ_S[0,1] = ⟨00|ψ⟩⟨ψ|10⟩ + ⟨01|ψ⟩⟨ψ|11⟩  (coherence)
    rho_00 = abs(psi[0]) ** 2 + abs(psi[1]) ** 2
    rho_11 = abs(psi[2]) ** 2 + abs(psi[3]) ** 2
    rho_01 = psi[0] * np.conj(psi[2]) + psi[1] * np.conj(psi[3])
    purity = rho_00**2 + rho_11**2 + 2.0 * abs(rho_01) ** 2
    # Assert the mathematically expected range [0.5, 1.0].
    # Outside this range indicates a computation error, not numerical noise.
    assert 0.5 - 1e-12 <= purity <= 1.0 + 1e-12, (
        f"Purity {purity:.15f} outside expected range [0.5, 1.0]"
    )
    return float(purity)


def compute_sensitivity(
    psi0: np.ndarray,
    T_BS1: float,
    T_BS2: float,
    t_hold: float,
    omega_true: float,
    alpha: tuple[float, float, float, float],
    ops: dict[str, np.ndarray],
    fd_step: float = 1e-6,
    meas_op: np.ndarray | None = None,
) -> float:
    """Compute the error-propagation sensitivity Δω.

    Δω = sqrt(Var(O)) / |∂⟨O⟩/∂ω|

    where O is the measurement operator (default: J_z^S).

    The derivative is computed via central finite differences.

    Args:
        psi0: Initial 4-vector (product state).
        T_BS1: First beam-splitter duration.
        T_BS2: Second beam-splitter duration.
        t_hold: Holding-time strength.
        omega_true: True phase rate parameter.
        alpha: (α_xx, α_xz, α_zx, α_zz) coupling coefficients.
        ops: Two-qubit operators (must contain 'Jz_S').
        fd_step: Finite-difference step size (default 1e-6).
        meas_op: Measurement operator. Defaults to ops['Jz_S'] (S-only).

    Returns:
        Sensitivity Δω (positive float).

    Raises:
        RuntimeError: If derivative is zero (operating at fringe extremum).

    """
    if meas_op is None:
        meas_op = ops["Jz_S"]

    # Evaluate at omega_true
    psi = evolve_full(psi0, T_BS1, T_BS2, t_hold, omega_true, alpha, ops)
    _, var = compute_expectation_and_variance(psi, meas_op)

    # Central finite difference for ∂⟨O⟩/∂θ
    psi_plus = evolve_full(psi0, T_BS1, T_BS2, t_hold, omega_true + fd_step, alpha, ops)
    psi_minus = evolve_full(
        psi0, T_BS1, T_BS2, t_hold, omega_true - fd_step, alpha, ops
    )
    exp_plus = np.real(psi_plus.conj() @ meas_op @ psi_plus)
    exp_minus = np.real(psi_minus.conj() @ meas_op @ psi_minus)
    d_exp = (exp_plus - exp_minus) / (2.0 * fd_step)

    if abs(d_exp) < 1e-12:
        # Derivative is effectively zero → fringe extremum
        return float("inf")

    return float(np.sqrt(var) / abs(d_exp))


# ============================================================================
# Objective Function for Optimisation
# ============================================================================


def _params_to_components(
    params: np.ndarray,
) -> tuple[
    float,
    float,
    float,
    float,
    float,
    float,
    float,
    tuple[float, float, float, float],
]:
    """Unpack the 11-element parameter vector into named components.

    Parameter order:
        [θ_S, φ_S, θ_A, φ_A, T_BS1, T_BS2, t_hold, α_xx, α_xz, α_zx, α_zz]
        Note: θ_S, θ_A are Bloch sphere polar angles, NOT the phase rate ω.

    Args:
        params: 11-element array of optimisation parameters.

    Returns:
        Tuple (theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, t_hold, alpha).

    """
    theta_S = float(params[0])
    phi_S = float(params[1])
    theta_A = float(params[2])
    phi_A = float(params[3])
    T_BS1 = float(params[4])
    T_BS2 = float(params[5])
    t_hold = float(params[6])
    alpha: tuple[float, float, float, float] = (
        float(params[7]),
        float(params[8]),
        float(params[9]),
        float(params[10]),
    )
    return theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, t_hold, alpha


def sensitivity_objective(
    params: np.ndarray,
    omega_true: float,
    ops: dict[str, np.ndarray],
    fd_step: float = 1e-6,
    bounds: dict[str, tuple[float, float]] | None = None,
    penalty_scale: float = 1e6,
    meas_op: np.ndarray | None = None,
    fixed_alpha: tuple[float, float, float, float] | None = None,
) -> float:
    """Objective function for minimising Δω.

    Args:
        params: 11-element optimisation parameter vector, or 7-element
            state-only vector when fixed_alpha is set.
        omega_true: True phase rate parameter.
        ops: Two-qubit operators.
        fd_step: Finite-difference step size.
        bounds: Dict of parameter bounds (defaults to report values).
        penalty_scale: Scale for bound-violation penalty.
        meas_op: Measurement operator. Defaults to ops['Jz_S'].
        fixed_alpha: If set, params is a 7-element state vector;
            fixed_alpha is prepended to form the full 11-element vector.
            Alpha bound checks are skipped in this mode.

    Returns:
        Δω value (plus infinite penalty if bounds violated).

    """
    # Default bounds from the report
    if bounds is None:
        bounds = get_default_bounds()

    # If fixed_alpha is set, params is 7-element (state only);
    # prepend fixed_alpha to make 11 elements.
    if fixed_alpha is not None:
        params = np.concatenate([params, np.asarray(fixed_alpha, dtype=float)])

    theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, t_hold, alpha = _params_to_components(
        params,
    )

    # --- Bound enforcement (penalty method) ---
    penalty = 0.0
    for val, key in [
        (theta_S, "bloch_theta"),
        (theta_A, "bloch_theta"),
        (phi_S, "phi"),
        (phi_A, "phi"),
        (T_BS1, "T_BS"),
        (T_BS2, "T_BS"),
        (t_hold, "t_hold"),
    ]:
        lo, hi = bounds[key]
        if val < lo:
            penalty += penalty_scale * (lo - val) ** 2
        if val > hi:
            penalty += penalty_scale * (val - hi) ** 2
    if fixed_alpha is None:
        for i in range(4):
            lo, hi = bounds["alpha"]
            if alpha[i] < lo:
                penalty += penalty_scale * (lo - alpha[i]) ** 2
            if alpha[i] > hi:
                penalty += penalty_scale * (alpha[i] - hi) ** 2

    if penalty > 0.0:
        return float(1e10 + penalty)

    # Build initial state
    psi0 = two_qubit_state(theta_S, phi_S, theta_A, phi_A)

    # Compute sensitivity
    return compute_sensitivity(
        psi0,
        T_BS1,
        T_BS2,
        t_hold,
        omega_true,
        alpha,
        ops,
        fd_step,
        meas_op=meas_op,
    )


