@lru_cache(maxsize=8)
def bs_unitary(T_BS: float) -> np.ndarray:
    """Single-qubit beam-splitter unitary generated by J_x.

    U_BS(T_BS) = exp(-i T_BS J_x) = cos(T_BS/2) I - i sin(T_BS/2) σ_x

    A 50/50 beam splitter corresponds to T_BS = π/2.

    Args:
        T_BS: Beam-splitter duration (optimisable parameter).

    Returns:
        2×2 unitary matrix.

    """
    return bs_qubit(T_BS)


@lru_cache(maxsize=8)
def two_qubit_bs_unitary(T_BS: float) -> np.ndarray:
    """Two-qubit beam-splitter unitary: U_BS(T_BS) ⊗ U_BS(T_BS).

    Args:
        T_BS: Beam-splitter duration.

    Returns:
        4×4 unitary matrix (tensor product).

    """
    return np.kron(bs_unitary(T_BS), bs_unitary(T_BS))


