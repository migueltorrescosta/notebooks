@dataclass
class AlphaReoptScanResult:
    """Result from scanning α with state re-optimisation at each point.

    Attributes:
        alpha_values: Array of α values scanned.
        delta_omega_joint: Best Δω for joint measurement at each α.
        delta_omega_sonly: Best Δω for S-only measurement at each α.
        best_params_joint: Full 11-element optimal params (joint) at each α.
        best_params_sonly: Full 11-element optimal params (S-only) at each α.

    """

    alpha_values: np.ndarray = field(default_factory=lambda: np.array([]))
    delta_omega_joint: np.ndarray = field(default_factory=lambda: np.array([]))
    delta_omega_sonly: np.ndarray = field(default_factory=lambda: np.array([]))
    best_params_joint: list[np.ndarray] = field(default_factory=list)
    best_params_sonly: list[np.ndarray] = field(default_factory=list)

    _PARAM_COLS: ClassVar[list[str]] = [
        "theta_S",
        "phi_S",
        "theta_A",
        "phi_A",
        "T_BS1",
        "T_BS2",
        "t_hold",
        "alpha_xx",
        "alpha_xz",
        "alpha_zx",
        "alpha_zz",
    ]

    def to_dataframe(self) -> pd.DataFrame:
        """Flatten the α re-optimisation scan into a DataFrame.

        Includes all metadata: optimal parameters for both joint and S-only
        measurements are stored in named columns alongside scalar results.

        Returns:
            DataFrame with columns: alpha, delta_omega_joint,
            delta_omega_sonly, and 22 optimal-parameter columns (11 per
            measurement type).

        """
        n = len(self.alpha_values)
        data: dict[str, object] = {
            "alpha": self.alpha_values,
            "delta_omega_joint": self.delta_omega_joint,
            "delta_omega_sonly": self.delta_omega_sonly,
        }

        # Pad or truncate params lists to match n
        def _pad(p: list[np.ndarray], n_target: int) -> list[np.ndarray]:
            if len(p) < n_target:
                return p + [np.full(11, float("nan"))] * (n_target - len(p))
            return p[:n_target]

        joint_params = _pad(self.best_params_joint, n)
        sonly_params = _pad(self.best_params_sonly, n)
        for i, col in enumerate(self._PARAM_COLS):
            data[f"joint_{col}"] = [float(p[i]) for p in joint_params]
            data[f"sonly_{col}"] = [float(p[i]) for p in sonly_params]
        return pd.DataFrame(data)

    def save_parquet(self, path: str | Path) -> Path:
        """Save to a Parquet file.

        Args:
            path: File path to write to.

        Returns:
            The path that was written to.
        """
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        self.to_dataframe().to_parquet(path, index=False)
        return path

    @classmethod
    def from_parquet(cls, path: str | Path) -> AlphaReoptScanResult:
        """Reconstruct from a Parquet file.

        Args:
            path: Path to the Parquet file.

        Returns:
            Reconstructed AlphaReoptScanResult.

        Raises:
            ValueError: If the Parquet file is missing required columns.
        """
        df = pd.read_parquet(path)
        required = {
            "alpha",
            "delta_omega_joint",
            "delta_omega_sonly",
        }
        for side in ("joint", "sonly"):
            for col in cls._PARAM_COLS:
                required.add(f"{side}_{col}")
        missing = required - set(df.columns)
        if missing:
            raise ValueError(
                f"Parquet at {path} is missing required columns: "
                f"{sorted(missing)}. Regenerate the file with the current code."
            )
        n = len(df)
        best_params_joint: list[np.ndarray] = [
            np.array([float(df[f"joint_{col}"].iloc[i]) for col in cls._PARAM_COLS])
            for i in range(n)
        ]
        best_params_sonly: list[np.ndarray] = [
            np.array([float(df[f"sonly_{col}"].iloc[i]) for col in cls._PARAM_COLS])
            for i in range(n)
        ]
        return cls(
            alpha_values=df["alpha"].to_numpy(dtype=float),
            delta_omega_joint=df["delta_omega_joint"].to_numpy(dtype=float),
            delta_omega_sonly=df["delta_omega_sonly"].to_numpy(dtype=float),
            best_params_joint=best_params_joint,
            best_params_sonly=best_params_sonly,
        )


def scan_alpha_with_reoptimisation(
    alpha_name: str,
    alpha_values: np.ndarray | None = None,
    *,
    omega_true: float = 1.0,
    n_restarts: int = 5,
    maxiter: int = 500,
    seed: int = 42,
) -> AlphaReoptScanResult:
    """Scan a single α coefficient with state re-optimisation at each point.

    For each α value, the state parameters (θ_S, φ_S, θ_A, φ_A, T_BS1,
    T_BS2, t_hold) are re-optimised via Nelder–Mead while the interaction
    coefficients are held fixed.  Both joint and S-only measurement
    operators are evaluated independently.

    Args:
        alpha_name: Which coefficient to scan: 'xx', 'xz', 'zx', or 'zz'.
        alpha_values: Array of α values to scan.  Defaults to 21 points
            in [-2.0, 2.0].
        omega_true: True phase rate parameter (default 1.0).
        n_restarts: Number of random-start Nelder–Mead runs per α.
        maxiter: Maximum Nelder–Mead iterations per run.
        seed: Base random seed (incremented per restart).

    Returns:
        AlphaReoptScanResult with all recorded information.

    Raises:
        ValueError: If alpha_name is not one of 'xx', 'xz', 'zx', 'zz'.

    """
    alpha_idx_map = {"xx": 0, "xz": 1, "zx": 2, "zz": 3}
    if alpha_name not in alpha_idx_map:
        raise ValueError(
            f"alpha_name must be one of {list(alpha_idx_map.keys())}, got {alpha_name}",
        )
    scan_idx = alpha_idx_map[alpha_name]

    if alpha_values is None:
        alpha_values = np.linspace(-2.0, 2.0, 21)

    ops = build_two_qubit_operators()
    M_op = build_joint_operator(ops)

    alpha_arr = np.asarray(alpha_values, dtype=float)
    n_points = len(alpha_arr)

    delta_omega_joint = np.full(n_points, np.inf, dtype=float)
    delta_omega_sonly = np.full(n_points, np.inf, dtype=float)
    best_params_joint: list[np.ndarray] = []
    best_params_sonly: list[np.ndarray] = []

    for i, a_val in enumerate(alpha_arr):
        # Build the fixed α tuple: one coefficient varies, others zero
        alpha_list = [0.0, 0.0, 0.0, 0.0]
        alpha_list[scan_idx] = a_val
        fixed_alpha: tuple[float, float, float, float] = (
            alpha_list[0],
            alpha_list[1],
            alpha_list[2],
            alpha_list[3],
        )

        # --- Joint measurement ---
        joint_results: list[OptimisationResult] = []
        for restart in range(n_restarts):
            rng = np.random.default_rng(seed + i * 1000 + restart)
            x0 = random_initial_params(rng)[:7]  # 7 state params
            opt = _run_nelder_mead(
                omega_true=omega_true,
                ops=ops,
                x0=x0,
                maxiter=maxiter,
                meas_op=M_op,
                fixed_alpha=fixed_alpha,
            )
            joint_results.append(opt)
        joint_results.sort(key=lambda r: r.delta_omega_opt)
        delta_omega_joint[i] = joint_results[0].delta_omega_opt
        best_params_joint.append(joint_results[0].params_opt.copy())

        # --- S-only measurement ---
        sonly_results: list[OptimisationResult] = []
        for restart in range(n_restarts):
            rng = np.random.default_rng(seed + i * 1000 + restart + 10000)
            x0 = random_initial_params(rng)[:7]
            opt = _run_nelder_mead(
                omega_true=omega_true,
                ops=ops,
                x0=x0,
                maxiter=maxiter,
                meas_op=None,
                fixed_alpha=fixed_alpha,
            )
            sonly_results.append(opt)
        sonly_results.sort(key=lambda r: r.delta_omega_opt)
        delta_omega_sonly[i] = sonly_results[0].delta_omega_opt
        best_params_sonly.append(sonly_results[0].params_opt.copy())

    return AlphaReoptScanResult(
        alpha_values=alpha_arr,
        delta_omega_joint=delta_omega_joint,
        delta_omega_sonly=delta_omega_sonly,
        best_params_joint=best_params_joint,
        best_params_sonly=best_params_sonly,
    )


