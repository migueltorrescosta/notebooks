@dataclass
class OptimisationResult:
    """Result of a single Nelder–Mead run.

    Attributes:
        delta_theta_opt: Best sensitivity Δθ found.
        meas_label: Measurement type — "S-only" or "Joint M".
        params_opt: Optimal 11-element parameter vector.
        theta_true: True θ used for this optimisation.
        success: Whether the optimiser reported success.
        nfev: Number of function evaluations.
        message: Optimiser message.
        expectation_Jz: ⟨J_z^S⟩ at the optimal operating point.
        variance_Jz: Var(J_z^S) at the optimal operating point.
        purity_S: Tr(ρ_S²) purity of the reduced system state (1 = pure,
            0.5 = maximally entangled with ancilla).
        expectation_M: ⟨J_z^S + J_z^A⟩ at the optimal operating point
            (for joint measurement).
        variance_M: Var(J_z^S + J_z^A) at the optimal operating point
            (for joint measurement).
        covariance_SA: Cov(J_z^S, J_z^A) at the optimal operating point
            (for joint measurement).
        history: Objective function values at each iteration (for
            convergence analysis). Empty list if callback not enabled.

    """

    delta_theta_opt: float
    params_opt: np.ndarray
    theta_true: float
    success: bool
    nfev: int
    message: str
    meas_label: str = "S-only"
    expectation_Jz: float = 0.0
    variance_Jz: float = 0.0
    purity_S: float = 0.0
    expectation_M: float = 0.0
    variance_M: float = 0.0
    covariance_SA: float = 0.0
    history: list[float] = field(default_factory=list)


@dataclass
class ThetaScanResult:
    """Results of a θ scan with multiple Nelder–Mead restarts per θ.

    Attributes:
        results: List of OptimisationResult per θ value.
        theta_values: Array of θ values scanned.
        best_per_theta: Best Δθ for each θ value.
        all_results: All results (including non-best restarts) keyed by θ.

    """

    results: list[OptimisationResult] = field(default_factory=list)
    theta_values: np.ndarray = field(default_factory=lambda: np.array([]))
    best_per_theta: np.ndarray = field(default_factory=lambda: np.array([]))
    all_results: dict[float, list[OptimisationResult]] = field(default_factory=dict)

    def to_dataframe(self) -> pd.DataFrame:
        """Flatten the θ-scan results into a tabular DataFrame.

        One row per θ value with the best result and derived quantities.
        Works both from full results (with ``all_results`` populated) and
        from CSV-loaded summary (using ``theta_values`` / ``best_per_theta``).

        Returns:
            DataFrame with columns: theta, best_delta_theta, sql, vs_sql,
            spread, T_H_star, covariance, expectation_M, flag.
        """
        rows: list[dict[str, float | str]] = []
        for i, theta in enumerate(self.theta_values):
            restarts = self.all_results.get(float(theta), [])
            if restarts:
                best = restarts[0]
                best_delta_theta = float(best.delta_theta_opt)
                spread = compute_convergence_metric(restarts)
                T_H_star = float(best.params_opt[6])
                cov_val = float(best.covariance_SA)
                exp_m = float(best.expectation_M)
                vs_sql = (
                    float(best.delta_theta_opt / T_H_star)
                    if T_H_star > 0 and np.isfinite(1.0 / T_H_star)
                    else float("inf")
                )
            else:
                # Summary-only mode (loaded from CSV)
                best_delta_theta = float(self.best_per_theta[i])
                spread = 0.0
                T_H_star = 0.0
                cov_val = 0.0
                exp_m = 0.0
                vs_sql = float("inf")

            sql = 1.0 / T_H_star if T_H_star > 0 else float("inf")
            flag = "fringe" if abs(exp_m) < 1e-10 else "ok"

            rows.append(
                {
                    "theta": float(theta),
                    "best_delta_theta": best_delta_theta,
                    "sql": sql,
                    "vs_sql": vs_sql,
                    "spread": spread,
                    "T_H_star": T_H_star,
                    "covariance": cov_val,
                    "expectation_M": exp_m,
                    "flag": flag,
                },
            )
        return pd.DataFrame(rows)

    def save_parquet(self, path: str | Path) -> Path:
        """Save the θ-scan summary to a Parquet file.

        Args:
            path: File path to write to.

        Returns:
            The path that was written to.
        """
        path = Path(path)
        path.parent.mkdir(parents=True, exist_ok=True)
        self.to_dataframe().to_parquet(path, index=False)
        return path

    @classmethod
    def from_parquet(cls, path: str | Path) -> ThetaScanResult:
        """Reconstruct a ThetaScanResult from a Parquet file written by ``save_parquet``.

        Note: The reconstructed result contains only the summary per θ value
        (one row = one θ), not the full per-restart details.

        Args:
            path: Path to the Parquet file.

        Returns:
            A ThetaScanResult with the summary data.
        """
        path = Path(path)
        df = pd.read_parquet(path)
        theta_values = df["theta"].to_numpy(dtype=float)
        best_per_theta = df["best_delta_theta"].to_numpy(dtype=float)
        return cls(
            results=[],
            theta_values=theta_values,
            best_per_theta=best_per_theta,
            all_results={},
        )


def get_default_bounds() -> dict[str, tuple[float, float]]:
    """Get the default parameter bounds.

    Returns:
        Dict with keys 'theta', 'phi', 'T_BS', 'T_H', 'alpha' mapping to
        (min, max) tuples.

    """
    return {
        "theta": (0.0, np.pi),
        "phi": (0.0, 2.0 * np.pi),
        "T_BS": (0.0, np.pi),
        "T_H": (0.0, 5.0),
        "alpha": (-2.0, 2.0),
    }


def random_initial_params(
    rng: np.random.Generator,
    bounds: dict[str, tuple[float, float]] | None = None,
) -> np.ndarray:
    """Generate random initial parameters within the search bounds.

    All 11 parameters are drawn independently from their respective bounds.

    Args:
        rng: Random number generator.
        bounds: Optional custom bounds (uses defaults if None).

    Returns:
        11-element array of initial parameters.

    """
    if bounds is None:
        bounds = get_default_bounds()

    theta_lo, theta_hi = bounds["theta"]
    phi_lo, phi_hi = bounds["phi"]
    tbs_lo, tbs_hi = bounds["T_BS"]
    th_lo, th_hi = bounds["T_H"]
    alpha_lo, alpha_hi = bounds["alpha"]

    theta_S, theta_A = rng.uniform(theta_lo, theta_hi, size=2)
    phi_S, phi_A = rng.uniform(phi_lo, phi_hi, size=2)
    T_BS1, T_BS2 = rng.uniform(tbs_lo, tbs_hi, size=2)
    T_H = rng.uniform(th_lo, th_hi)
    alpha = rng.uniform(alpha_lo, alpha_hi, size=4)
    return np.array(
        [theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, T_H, *alpha],
        dtype=float,
    )


def run_optimisation(
    theta_true: float,
    ops: dict[str, np.ndarray],
    x0: np.ndarray | None = None,
    seed: int | None = None,
    maxiter: int = 5000,
    xatol: float = 1e-8,
    fatol: float = 1e-8,
    adaptive: bool = True,
    bounds: dict[str, tuple[float, float]] | None = None,
    track_history: bool = False,
    meas_op: np.ndarray | None = None,
    fixed_alpha: tuple[float, float, float, float] | None = None,
) -> OptimisationResult:
    """Run a single Nelder–Mead optimisation for ancilla-assisted metrology.

    Args:
        theta_true: True phase rate parameter.
        ops: Two-qubit operators.
        x0: Initial parameter vector (11 elements, or 7 when fixed_alpha is set).
            Random if None.
        seed: Random seed (used if x0 is None).
        maxiter: Maximum Nelder–Mead iterations.
        xatol: Absolute parameter tolerance.
        fatol: Absolute function tolerance.
        adaptive: Use adaptive Nelder–Mead parameters.
        bounds: Custom parameter bounds dictionary. Uses defaults if None.
        track_history: If True, record objective function values at each
            iteration in the result's `history` field.
        meas_op: Measurement operator for the objective.
            Defaults to ops['Jz_S'] (S-only).
        fixed_alpha: If set, optimisation is over state params only (7 elements)
            with the interaction coefficients held fixed.

    Returns:
        OptimisationResult with the best parameters found.

    """
    return _run_nelder_mead(
        theta_true=theta_true,
        ops=ops,
        x0=x0,
        seed=seed,
        maxiter=maxiter,
        xatol=xatol,
        fatol=fatol,
        adaptive=adaptive,
        bounds=bounds,
        track_history=track_history,
        meas_op=meas_op,
        fixed_alpha=fixed_alpha,
    )


def diagnostics_from_params(
    params: np.ndarray,
    theta_true: float,
    ops: dict[str, np.ndarray],
) -> tuple[float, float, float, float, float, float]:
    """Compute diagnostics (expectations, variances, covariance, purity) at a given parameter vector.

    Args:
        params: 11-element parameter vector (θ_S, φ_S, θ_A, φ_A, T_BS1, T_BS2, T_H, α_xx, α_xz, α_zx, α_zz).
        theta_true: True phase rate parameter.
        ops: Two-qubit operators from build_two_qubit_operators().

    Returns:
        Tuple (exp_Jz, var_Jz, exp_M, var_M, cov_SA, purity).

    """
    (
        opt_theta_S,
        opt_phi_S,
        opt_theta_A,
        opt_phi_A,
        opt_T_BS1,
        opt_T_BS2,
        opt_T_H,
        opt_alpha,
    ) = _params_to_components(params)
    opt_psi0 = two_qubit_state(opt_theta_S, opt_phi_S, opt_theta_A, opt_phi_A)
    opt_psi = evolve_full(
        opt_psi0,
        opt_T_BS1,
        opt_T_BS2,
        opt_T_H,
        theta_true,
        opt_alpha,
        ops,
    )

    # Always compute both S-only and joint measurement diagnostics
    exp_val_s, var_val_s = compute_expectation_and_variance(opt_psi, ops["Jz_S"])
    M_op = build_joint_operator(ops)
    exp_val_m, var_val_m = compute_expectation_and_variance(opt_psi, M_op)
    cov_sa = compute_covariance(opt_psi, ops)
    purity = compute_reduced_purity(opt_psi)

    return exp_val_s, var_val_s, exp_val_m, var_val_m, cov_sa, purity


def _run_nelder_mead(
    theta_true: float,
    ops: dict[str, np.ndarray],
    x0: np.ndarray | None = None,
    seed: int | None = None,
    maxiter: int = 5000,
    xatol: float = 1e-8,
    fatol: float = 1e-8,
    adaptive: bool = True,
    bounds: dict[str, tuple[float, float]] | None = None,
    track_history: bool = False,
    meas_op: np.ndarray | None = None,
    fixed_alpha: tuple[float, float, float, float] | None = None,
) -> OptimisationResult:
    """Internal Nelder–Mead runner. Shared by run_optimisation and run_theta_scan.

    Args:
        theta_true: True phase rate parameter.
        ops: Two-qubit operators.
        x0: Initial parameter vector (11 elements, or 7 when fixed_alpha is set).
            Random if None.
        seed: Random seed (used if x0 is None).
        maxiter: Maximum Nelder–Mead iterations.
        xatol: Absolute parameter tolerance.
        fatol: Absolute function tolerance.
        adaptive: Use adaptive Nelder–Mead parameters.
        bounds: Custom parameter bounds dictionary. Uses defaults if None.
        track_history: If True, record objective function values per iteration.
        meas_op: Measurement operator for the objective.
            Defaults to ops['Jz_S'] (S-only).
        fixed_alpha: If set, optimisation is over state params only (7 elements)
            with the interaction coefficients held fixed.

    """
    if x0 is None:
        rng = np.random.default_rng(seed)
        x0 = random_initial_params(rng, bounds)
    else:
        x0 = np.asarray(x0, dtype=float)

    if fixed_alpha is not None:
        assert x0.shape in ((11,), (7,)), (
            f"x0 must have 7 or 11 elements when fixed_alpha is set, got {x0.shape}"
        )
        x0 = x0[:7]  # keep only state params
    else:
        assert x0.shape == (11,), "x0 must have 11 elements"

    # Objective function (closure over theta_true, ops, bounds, meas_op, fixed_alpha)
    def objective(p: np.ndarray) -> float:
        return sensitivity_objective(
            p, theta_true, ops, bounds=bounds, meas_op=meas_op, fixed_alpha=fixed_alpha
        )

    # History tracking via callback
    history: list[float] = []

    def callback(_x: np.ndarray) -> None:
        if track_history:
            val = objective(_x)
            history.append(val)

    result = minimize(
        objective,
        x0=x0,
        method="Nelder-Mead",
        callback=callback if track_history else None,  # type: ignore[arg-type]
        options={  # type: ignore[call-overload]
            "maxiter": maxiter,
            "xatol": xatol,
            "fatol": fatol,
            "adaptive": adaptive,
        },
    )

    # Compute expectation, variance, and purity at the optimal point
    # Use the full 11-element parameter vector for diagnostics
    opt_params = result.x.copy()
    if fixed_alpha is not None:
        opt_params = np.concatenate([opt_params, np.asarray(fixed_alpha, dtype=float)])
    (
        exp_val_s,
        var_val_s,
        exp_val_m,
        var_val_m,
        cov_sa,
        purity,
    ) = diagnostics_from_params(opt_params, theta_true, ops)

    # Determine measurement label by comparing meas_op to known operators
    if meas_op is None:
        meas_label = "S-only"
    else:
        # Compare against the canonical joint operator Jz_S + Jz_A
        joint_op = build_joint_operator(ops)
        if np.allclose(meas_op, joint_op, atol=1e-12):
            meas_label = "Joint M"
        else:
            meas_label = "Custom"

    return OptimisationResult(
        delta_theta_opt=float(result.fun),
        meas_label=meas_label,
        params_opt=opt_params,
        theta_true=theta_true,
        success=bool(result.success),
        nfev=int(result.nfev),
        message=str(result.message),
        expectation_Jz=exp_val_s,
        variance_Jz=var_val_s,
        purity_S=purity,
        expectation_M=exp_val_m,
        variance_M=var_val_m,
        covariance_SA=cov_sa,
        history=history.copy(),
    )


def run_theta_scan(
    theta_values: list[float] | np.ndarray,
    n_restarts: int = 10,
    seed: int | None = 42,
    maxiter: int = 5000,
    xatol: float = 1e-8,
    fatol: float = 1e-8,
    adaptive: bool = True,
    bounds: dict[str, tuple[float, float]] | None = None,
    track_history: bool = False,
    meas_op: np.ndarray | None = None,
) -> ThetaScanResult:
    """Scan over θ values with multiple Nelder–Mead restarts each.

    Args:
        theta_values: θ values to scan.
        n_restarts: Number of random-start Nelder–Mead runs per θ.
        seed: Base random seed (incremented per restart).
        maxiter: Maximum Nelder–Mead iterations per run.
        xatol: Absolute parameter tolerance.
        fatol: Absolute function tolerance.
        adaptive: Use adaptive Nelder–Mead parameters.
        bounds: Custom parameter bounds (e.g., to expand T_H range).
            Default: T_H ∈ [0, 5]. Use bounds={"T_H": (0, 20), ...} for
            expanded-range experiments.
        track_history: If True, record convergence history per run.
        meas_op: Measurement operator for the objective.
            Defaults to ops['Jz_S'] (S-only).

    Returns:
        ThetaScanResult with all recorded information.

    """
    ops = build_two_qubit_operators()
    theta_arr = np.asarray(theta_values, dtype=float)
    base_seed = seed if seed is not None else 42

    all_results: dict[float, list[OptimisationResult]] = {}
    best_per_theta_list: list[float] = []
    all_results_flat: list[OptimisationResult] = []

    for theta in theta_arr:
        theta_results: list[OptimisationResult] = []
        for restart in range(n_restarts):
            rng = np.random.default_rng(base_seed + int(theta * 1000) + restart)
            x0 = random_initial_params(rng, bounds)
            opt_result = _run_nelder_mead(
                theta_true=theta,
                ops=ops,
                x0=x0,
                maxiter=maxiter,
                xatol=xatol,
                fatol=fatol,
                adaptive=adaptive,
                bounds=bounds,
                track_history=track_history,
                meas_op=meas_op,
            )
            theta_results.append(opt_result)
            all_results_flat.append(opt_result)

        # Sort by sensitivity (ascending)
        theta_results.sort(key=lambda r: r.delta_theta_opt)
        all_results[theta] = theta_results
        best_per_theta_list.append(theta_results[0].delta_theta_opt)

    return ThetaScanResult(
        results=all_results_flat,
        theta_values=theta_arr,
        best_per_theta=np.array(best_per_theta_list),
        all_results=all_results,
    )


