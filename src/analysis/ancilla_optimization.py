@dataclass
class OptimisationResult:
    """Result of a single Nelder–Mead run.

    Attributes:
        delta_theta_opt: Best sensitivity Δθ found.
        params_opt: Optimal 11-element parameter vector.
        theta_true: True θ used for this optimisation.
        success: Whether the optimiser reported success.
        nfev: Number of function evaluations.
        message: Optimiser message.
        expectation_Jz: ⟨J_z^S⟩ at the optimal operating point.
        variance_Jz: Var(J_z^S) at the optimal operating point.
        purity_S: Tr(ρ_S²) purity of the reduced system state (1 = pure,
            0.5 = maximally entangled with ancilla).
        history: Objective function values at each iteration (for
            convergence analysis). Empty list if callback not enabled.
    """

    delta_theta_opt: float
    params_opt: np.ndarray
    theta_true: float
    success: bool
    nfev: int
    message: str
    expectation_Jz: float = 0.0
    variance_Jz: float = 0.0
    purity_S: float = 0.0
    history: list[float] = field(default_factory=list)


@dataclass
class ThetaScanResult:
    """Results of a θ scan with multiple Nelder–Mead restarts per θ.

    Attributes:
        results: List of OptimisationResult per θ value.
        theta_values: Array of θ values scanned.
        best_per_theta: Best Δθ for each θ value.
        all_results: All results (including non-best restarts) keyed by θ.
    """

    results: list[OptimisationResult] = field(default_factory=list)
    theta_values: np.ndarray = field(default_factory=lambda: np.array([]))
    best_per_theta: np.ndarray = field(default_factory=lambda: np.array([]))
    all_results: dict[float, list[OptimisationResult]] = field(default_factory=dict)


def get_default_bounds() -> dict[str, tuple[float, float]]:
    """Get the default parameter bounds.

    Returns:
        Dict with keys 'theta', 'phi', 'T_BS', 'T_H', 'alpha' mapping to
        (min, max) tuples.
    """
    return {
        "theta": (0.0, np.pi),
        "phi": (0.0, 2.0 * np.pi),
        "T_BS": (0.0, np.pi),
        "T_H": (0.0, 5.0),
        "alpha": (-2.0, 2.0),
    }


def random_initial_params(
    rng: np.random.Generator, bounds: dict[str, tuple[float, float]] | None = None
) -> np.ndarray:
    """Generate random initial parameters within the search bounds.

    All 11 parameters are drawn independently from their respective bounds.

    Args:
        rng: Random number generator.
        bounds: Optional custom bounds (uses defaults if None).

    Returns:
        11-element array of initial parameters.
    """
    if bounds is None:
        bounds = get_default_bounds()

    theta_lo, theta_hi = bounds["theta"]
    phi_lo, phi_hi = bounds["phi"]
    tbs_lo, tbs_hi = bounds["T_BS"]
    th_lo, th_hi = bounds["T_H"]
    alpha_lo, alpha_hi = bounds["alpha"]

    theta_S, theta_A = rng.uniform(theta_lo, theta_hi, size=2)
    phi_S, phi_A = rng.uniform(phi_lo, phi_hi, size=2)
    T_BS1, T_BS2 = rng.uniform(tbs_lo, tbs_hi, size=2)
    T_H = rng.uniform(th_lo, th_hi)
    alpha = rng.uniform(alpha_lo, alpha_hi, size=4)
    return np.array(
        [theta_S, phi_S, theta_A, phi_A, T_BS1, T_BS2, T_H, *alpha], dtype=float
    )


def run_optimisation(
    theta_true: float,
    ops: dict[str, np.ndarray],
    x0: np.ndarray | None = None,
    seed: int | None = None,
    maxiter: int = 5000,
    xatol: float = 1e-8,
    fatol: float = 1e-8,
    adaptive: bool = True,
    bounds: dict[str, tuple[float, float]] | None = None,
    track_history: bool = False,
) -> OptimisationResult:
    """Run a single Nelder–Mead optimisation for ancilla-assisted metrology.

    Args:
        theta_true: True phase rate parameter.
        ops: Two-qubit operators.
        x0: Initial parameter vector (11 elements). Random if None.
        seed: Random seed (used if x0 is None).
        maxiter: Maximum Nelder–Mead iterations.
        xatol: Absolute parameter tolerance.
        fatol: Absolute function tolerance.
        adaptive: Use adaptive Nelder–Mead parameters.
        bounds: Custom parameter bounds dictionary. Uses defaults if None.
        track_history: If True, record objective function values at each
            iteration in the result's `history` field.

    Returns:
        OptimisationResult with the best parameters found.
    """
    return _run_nelder_mead(
        theta_true=theta_true,
        ops=ops,
        x0=x0,
        seed=seed,
        maxiter=maxiter,
        xatol=xatol,
        fatol=fatol,
        adaptive=adaptive,
        bounds=bounds,
        track_history=track_history,
    )


def _run_nelder_mead(
    theta_true: float,
    ops: dict[str, np.ndarray],
    x0: np.ndarray | None = None,
    seed: int | None = None,
    maxiter: int = 5000,
    xatol: float = 1e-8,
    fatol: float = 1e-8,
    adaptive: bool = True,
    bounds: dict[str, tuple[float, float]] | None = None,
    track_history: bool = False,
) -> OptimisationResult:
    """Internal Nelder–Mead runner. Shared by run_optimisation and run_theta_scan.

    Args:
        theta_true: True phase rate parameter.
        ops: Two-qubit operators.
        x0: Initial parameter vector (11 elements). Random if None.
        seed: Random seed (used if x0 is None).
        maxiter: Maximum Nelder–Mead iterations.
        xatol: Absolute parameter tolerance.
        fatol: Absolute function tolerance.
        adaptive: Use adaptive Nelder–Mead parameters.
        bounds: Custom parameter bounds dictionary. Uses defaults if None.
        track_history: If True, record objective function values per iteration.
    """
    if x0 is None:
        rng = np.random.default_rng(seed)
        x0 = random_initial_params(rng, bounds)
    else:
        x0 = np.asarray(x0, dtype=float)
        assert x0.shape == (11,), "x0 must have 11 elements"

    # Objective function (closure over theta_true, ops, bounds)
    def objective(p: np.ndarray) -> float:
        return sensitivity_objective(p, theta_true, ops, bounds=bounds)

    # History tracking via callback
    history: list[float] = []

    def callback(_x: np.ndarray) -> None:
        if track_history:
            val = objective(_x)
            history.append(val)

    result = minimize(
        objective,
        x0=x0,
        method="Nelder-Mead",
        callback=callback if track_history else None,
        options={  # type: ignore[call-overload]
            "maxiter": maxiter,
            "xatol": xatol,
            "fatol": fatol,
            "adaptive": adaptive,
        },
    )

    # Compute expectation, variance, and purity at the optimal point
    opt_params = result.x.copy()
    (
        opt_theta_S,
        opt_phi_S,
        opt_theta_A,
        opt_phi_A,
        opt_T_BS1,
        opt_T_BS2,
        opt_T_H,
        opt_alpha,
    ) = _params_to_components(opt_params)
    opt_psi0 = two_qubit_state(opt_theta_S, opt_phi_S, opt_theta_A, opt_phi_A)
    opt_psi = evolve_full(
        opt_psi0, opt_T_BS1, opt_T_BS2, opt_T_H, theta_true, opt_alpha, ops
    )
    exp_val, var_val = compute_expectation_and_variance(opt_psi, ops["Jz_S"])
    purity = compute_reduced_purity(opt_psi)

    return OptimisationResult(
        delta_theta_opt=float(result.fun),
        params_opt=opt_params,
        theta_true=theta_true,
        success=bool(result.success),
        nfev=int(result.nfev),
        message=str(result.message),
        expectation_Jz=exp_val,
        variance_Jz=var_val,
        purity_S=purity,
        history=history.copy(),
    )


def run_theta_scan(
    theta_values: list[float] | np.ndarray,
    n_restarts: int = 10,
    seed: int | None = 42,
    maxiter: int = 5000,
    xatol: float = 1e-8,
    fatol: float = 1e-8,
    adaptive: bool = True,
    bounds: dict[str, tuple[float, float]] | None = None,
    track_history: bool = False,
) -> ThetaScanResult:
    """Scan over θ values with multiple Nelder–Mead restarts each.

    Args:
        theta_values: θ values to scan.
        n_restarts: Number of random-start Nelder–Mead runs per θ.
        seed: Base random seed (incremented per restart).
        maxiter: Maximum Nelder–Mead iterations per run.
        xatol: Absolute parameter tolerance.
        fatol: Absolute function tolerance.
        adaptive: Use adaptive Nelder–Mead parameters.
        bounds: Custom parameter bounds (e.g., to expand T_H range).
            Default: T_H ∈ [0, 5]. Use bounds={"T_H": (0, 20), ...} for
            expanded-range experiments.
        track_history: If True, record convergence history per run.

    Returns:
        ThetaScanResult with all recorded information.
    """
    ops = build_two_qubit_operators()
    theta_arr = np.asarray(theta_values, dtype=float)
    base_seed = seed if seed is not None else 42

    all_results: dict[float, list[OptimisationResult]] = {}
    best_per_theta_list: list[float] = []
    all_results_flat: list[OptimisationResult] = []

    for theta in theta_arr:
        theta_results: list[OptimisationResult] = []
        for restart in range(n_restarts):
            rng = np.random.default_rng(base_seed + int(theta * 1000) + restart)
            x0 = random_initial_params(rng, bounds)
            opt_result = _run_nelder_mead(
                theta_true=theta,
                ops=ops,
                x0=x0,
                maxiter=maxiter,
                xatol=xatol,
                fatol=fatol,
                adaptive=adaptive,
                bounds=bounds,
                track_history=track_history,
            )
            theta_results.append(opt_result)
            all_results_flat.append(opt_result)

        # Sort by sensitivity (ascending)
        theta_results.sort(key=lambda r: r.delta_theta_opt)
        all_results[theta] = theta_results
        best_per_theta_list.append(theta_results[0].delta_theta_opt)

    return ThetaScanResult(
        results=all_results_flat,
        theta_values=theta_arr,
        best_per_theta=np.array(best_per_theta_list),
        all_results=all_results,
    )


