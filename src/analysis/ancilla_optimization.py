def compute_convergence_metric(
    results: list[OptimisationResult],
) -> float:
    """Compute the convergence spread metric for a set of restarts.

    Returns std(Δθ) / mean(Δθ) for the given results. A value < 0.10
    indicates consistent convergence across restarts (Success Criterion #1).

    Args:
        results: List of OptimisationResult for a given θ (all restarts).

    Returns:
        Relative standard deviation (spread / mean). Returns 0.0 if
        fewer than 2 results are provided or mean is zero.
    """
    if len(results) < 2:
        return 0.0
    deltas = np.array([r.delta_theta_opt for r in results])
    # Filter out infinite values (fringe-extremum results)
    finite = deltas[np.isfinite(deltas)]
    if len(finite) < 2:
        return 0.0
    mean_val = float(np.mean(finite))
    if mean_val == 0.0:
        return 0.0
    return float(np.std(finite, ddof=1) / mean_val)


def get_decoupled_sensitivity(T_H: float, theta_true: float = 1.0) -> float:
    """Compute the SQL sensitivity for the decoupled (α=0) case.

    Optimal configuration: system in |1,0⟩, 50/50 beam splitters, no interaction.

    Args:
        T_H: Holding-time strength.
        theta_true: True phase rate.

    Returns:
        Δθ (ideally = 1/T_H).
    """
    ops = build_two_qubit_operators()
    psi0 = two_qubit_state(0.0, 0.0, 0.0, 0.0)  # both in |1,0⟩
    T_BS = np.pi / 2.0
    alpha = (0.0, 0.0, 0.0, 0.0)
    return compute_sensitivity(psi0, T_BS, T_BS, T_H, theta_true, alpha, ops)


